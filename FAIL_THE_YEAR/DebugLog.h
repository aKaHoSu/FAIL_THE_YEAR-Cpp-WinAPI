/* =============================================================================
 * ファイル概要:
 * - デバッグ時のみ有効なログ出力マクロを定義する。
 * ============================================================================= */
#pragma once

#ifdef _DEBUG
#include <cstdio>

// デバッグビルドのみ
// 標準出力にログを出力する
#define DEBUG_LOG(fmt, ...) printf(fmt, ##__VA_ARGS__)

#else
// リリースビルドでは完全に消える
#define DEBUG_LOG(fmt, ...) ((void)0)
#endif
